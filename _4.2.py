a = input("Введите 5-ти значное число:")
# Преобразуем 5-ти значное числ в 5 разных чисел: b2,c2,d2,e2 и e1.
b1 = float(a) // 10
b2 = float(a) % b1
c1 = b1 // 10
c2 = b1 % c1
d1 = c1 // 10
d2 = c1 % d1
e1 = d1 // 10
e2 = d1 % 10

# Алгоритм нахождения искомого числа
number = (c2 ** b2) * d2 / (e1 - e2)
print(number)